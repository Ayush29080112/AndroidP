package in.ayush.recyclerview;

public class SimpleExampleItem {
    private int mImageRes;
    private String mText;

    public SimpleExampleItem(int mImageRes, String mText) {
        this.mImageRes = mImageRes;
        this.mText = mText;
    }

    public int getmImageRes() {
        return mImageRes;
    }

    public String getmText() {
        return mText;
    }
}
